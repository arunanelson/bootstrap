          <div class="article_title w350">The History Of Man Making Glass</div>
          <div class="article_body w640 fleft">
            <p>Truth be told, Mother Nature made the first glass: obsidian, 
              the glisteningly black igneous rock formed when lava quickly cooled millions of years ago. 
              Used to create spearheads by Stone Age hunters, it was traded, protected and treasured. 
              Man stepped in and began creating glass to order around 5,000 years ago in Mesopotamia, 
              the area that lies between the Tigris and Euphrates rivers in what is now Syria, Iran and Iraq. 
              Some of the first known glass objects were beads, with glasses, plates and carafes not showing 
              up on the scene until about 1,200 years later. The earliest pieces used for food storage 
              were made one of two ways: either coils of hot molten glass built up on a form in a process 
              similar to coil built pottery, or carved from solid blocks using the same techniques familiar 
              to artisans accustomed to carving stone. </p>
            <p>A few hundred years later, glass production moved beyond its area of origin. 
              Archeologists have discovered artifacts in Crete, Egypt and Western Asia. 
              Typically found associated with royal palaces and estates, glass was a luxury and it is 
              speculated that the "recipe" was a closely guarded secret. </p>
            <p>By the 6th century BCE, artisans had codified the steps used to produce glass on stone tablets. 
              Techniques used for the next few hundred years were similar in many cultures and produced utilitarian objects. 
              In addition, glass tiles were created and used in the decorative arts.  
              By the first century CE, the industry changed dramatically with the discovery of glass blowing. 
              For the first time, a faster method of creating vessels was available and glass, 
              though still a luxury item, was more readily available. </p>
            <p>Traded around the world, there is evidence of Roman glass making it's way to China before the common era, 
              but production being ramped up with blown glass is the beginning of it taking off as a real industry. </p>
