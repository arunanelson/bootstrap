          <div class="article_title w310">The History OF Italian Glass</div>
          <div class="article_body w640 fleft">
            <p>For several hundreds of years after the first century, 
              most glass making rolled along with pretty much the same number of advances across what is now Europe.  
              There is archeological evidence from the 6th century supporting the evidence of glass making in Venice, 
              primarily tiles used decoratively and as a means to bring light into bath houses. Venice's unique geographic 
              location on the Adriatic as the "land and water bridge" between what is now Italy and the Near East meant it was 
              the center of an enormous amount of trade, both land and sea based. This gave the local glass artisans the advantage 
              of a huge customer base, and Venice dominated in glass production for hundreds of years. In addition, 
              Venetians were the first to experience the advanced glass techniques being used in the East. During the 
              Crusades, many Byzantine glass artists immigrated to Venice and set up shop. Glass became a major economic force, 
              so much so that in the late 13th century the Republic of Venice decreed that no foreign glass could be imported and 
              foreign glassmakers were no longer allowed to set up shop in the republic.</p>
            <p>Around this time, the mainly wooden glass shops were considered a fire hazard and all glass making 
              operations were moved to the island of Murano, just off the coast of Venice. 
              This edict was a bit of a sham, as the main reasoning behind the move was to isolate the 
              glassmakers from the rest of the world and vice versa. No secrets out, no spies in, and, later, 
              no glassmakers allowed to move off the island. The powers that be in Venice were very serious 
              about their glass. As in all towns, the glass artisans were part of a guild, the trade union/secret society of the time.</p>
            <p>In addition to Murano, Altare, just off the Mediterranean coast of the Italian Riviera 
              became the other great glass city if Italy. Legend has it that in the 11th century a 
              hermit was so impressed with the wood on the coast that he convinced some families to 
              establish the area as a glass-making center. The Bormioli family was among them, and thus a dynasty was born.</p>
